    @Override
    public T remove(int index) {
        Node<T> temp = header;
        if (index == 0) {
            if (header == footer) {
                header = null;
                footer = null;
            }
            header = header.getNext();

        } else {
            for (int i = 0; i < index - 1; i++) {
                temp = temp.getNext();
            }
            temp.setNext(temp);
        }
        return null;
    }

