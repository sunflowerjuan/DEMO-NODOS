    private Node<T> header = null;
    private Node<T> footer = null;

    public boolean add(T info) {

        Node<T> tempNode = new Node();
        tempNode.setInfo(info);

        if (header == null) {
            header = tempNode;
            footer = header;
        } else {
            footer.setNext(tempNode);
            footer = tempNode;
        }
        return false;
    }

